package chash4j;

public class CHashException extends Exception {

    public CHashException() {
        super();
    }

    public CHashException(String message) {
        super(message);
    }

    public CHashException(String message, Throwable cause) {
        super(message, cause);
    }

    public CHashException(Throwable cause) {
        super(cause);
    }

}
